/** Enum of all the different valid values for the "variant" prop on the "Button" component. */
export enum DsButtonVariant {
  /** This will give the button straight edges with slightly rounded corners. */
  BOXED = 'BOXED',
  /** This will give the button half-circular horizontal edges */
  ROUNDED = 'ROUNDED',
}

/** Enum of all the different valid values for the "size" prop on the "Button" component. */
export enum DsButtonSize {
  /** Self-explanatory */
  LARGE = 'LARGE',
  /** Self-explanatory */
  MEDIUM = 'MEDIUM',
  /** Self-explanatory */
  SMALL = 'SMALL',
}
